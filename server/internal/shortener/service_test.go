func TestCreate_AliasTaken_NoRetry(t *testing.T) {
	repo := &mockURLRepo{existsFunc: func(string) (bool, error) { return true, nil }}
	svc := newSvc(repo, newMockCache())

	alias := "tomado"
	_, err := svc.CreateShortURL(context.Background(), "owner1", "https://go.dev", &alias)
	if !errors.Is(err, ErrAliasTaken) {
		t.Fatalf("esperaba ErrAliasTaken, obtuvo %v", err)
	}
	if repo.createCalls != 0 {
		t.Fatalf("no debía intentar crear con alias tomado, hubo %d Create", repo.createCalls)
	}
	if repo.existsCalls != 1 {
		t.Fatalf("alias tomado no debe reintentar: esperaba 1 ExistsByCode, hubo %d", repo.existsCalls)
	}
}

