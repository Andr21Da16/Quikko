type mockCache struct {
	store   map[string]string
	setKeys []string
	delKeys []string
}

func newMockCache() *mockCache {
	return &mockCache{store: map[string]string{}}
}
func (m *mockCache) Set(_ context.Context, code, originalURL string) error {
	m.store[code] = originalURL
	m.setKeys = append(m.setKeys, code)
	return nil
}
func (m *mockCache) Get(_ context.Context, code string) (string, error) {
	v, ok := m.store[code]
	if !ok {
		return "", ErrCacheMiss
	}
	return v, nil
}
func (m *mockCache) Delete(_ context.Context, code string) error {
	delete(m.store, code)
	m.delKeys = append(m.delKeys, code)
	return nil
}

// --- Tests de CreateShortURL ---

func TestCreate_Autogenerated_Success(t *testing.T) {
	repo := &mockURLRepo{existsFunc: func(string) (bool, error) { return false, nil }}
	cache := newMockCache()
	svc := newSvc(repo, cache)

	url, err := svc.CreateShortURL(context.Background(), "owner1", "https://go.dev", nil)
	if err != nil {
		t.Fatalf("esperaba éxito, obtuvo %v", err)
	}
	if url.ShortCode == "" || url.IsCustomAlias {
		t.Fatalf("código autogenerado inesperado: %+v", url)
	}
	if repo.createCalls != 1 {
		t.Fatalf("esperaba 1 Create, hubo %d", repo.createCalls)
	}
	if cache.store[url.ShortCode] != "https://go.dev" {
		t.Fatal("el cache no se pobló tras crear")
	}
}

