func TestCheckAlias(t *testing.T) {
	repo := &mockURLRepo{}
	svc := newSvc(repo, newMockCache())

	repo.existsFunc = func(string) (bool, error) { return true, nil }
	if avail, _ := svc.CheckAliasAvailability(context.Background(), "tomado"); avail {
		t.Fatal("alias existente debería ser no disponible")
	}
	repo.existsFunc = func(string) (bool, error) { return false, nil }
	if avail, _ := svc.CheckAliasAvailability(context.Background(), "libre"); !avail {
		t.Fatal("alias inexistente debería estar disponible")
	}
	// Formato inválido: no disponible y sin tocar el repo.
	if avail, _ := svc.CheckAliasAvailability(context.Background(), "x"); avail {
		t.Fatal("alias con formato inválido no debe estar disponible")
	}
}

func TestToggleActive_DeactivateRemovesFromCache(t *testing.T) {
	owned := &ShortURL{ID: "id1", ShortCode: "abc123", OriginalURL: "https://go.dev", OwnerID: "owner1", IsActive: true}
	repo := &mockURLRepo{findByID: func(string) (*ShortURL, error) { return owned, nil }}
	cache := newMockCache()
	cache.store["abc123"] = "https://go.dev"
	svc := newSvc(repo, cache)

	if err := svc.ToggleActive(context.Background(), "owner1", "id1", false); err != nil {
		t.Fatalf("toggle falló: %v", err)
	}
	if _, ok := cache.store["abc123"]; ok {
		t.Fatal("al desactivar, la entrada debe desaparecer de Redis")
	}
	if len(cache.delKeys) != 1 || cache.delKeys[0] != "abc123" {
		t.Fatalf("esperaba Delete del código en cache, hubo %v", cache.delKeys)
	}
}

